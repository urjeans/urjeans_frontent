const switcher = document.getElementById("languageSwitcher");

function updateLanguage(lang) {
  const elements = document.querySelectorAll("[data-translate]");
  elements.forEach((el) => {
    const key = el.getAttribute("data-translate");
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll('.selected-language').forEach(selectedLanguage => {
    let flagSrc = '';
    if (lang === 'uz') flagSrc = '/assets/flags/uz.svg';
    if (lang === 'en') flagSrc = '/assets/flags/us.svg';
    if (lang === 'ru') flagSrc = '/assets/flags/ru.svg';
    selectedLanguage.innerHTML = `<img src="${flagSrc}" alt="">${lang.toUpperCase()}`;
  });

  document.querySelectorAll('.language-options').forEach(options => {
    options.querySelectorAll('li').forEach(li => {
      li.classList.toggle('active', li.getAttribute('data-lang') === lang);
    });
  });

  localStorage.setItem('lang', lang);
}

document.querySelectorAll('.language-selector').forEach(selector => {
  const selectedLanguage = selector.querySelector('.selected-language');
  const languageOptions = selector.querySelector('.language-options');

  selectedLanguage.addEventListener('click', function (e) {
    document.querySelectorAll('.language-selector').forEach(s => s.classList.remove('open'));
    selector.classList.toggle('open');
    e.stopPropagation();
  });

  languageOptions.querySelectorAll('li').forEach(item => {
    item.addEventListener('click', function () {
      const lang = item.getAttribute('data-lang');
      updateLanguage(lang);
      selector.classList.remove('open');
    });
  });
});


document.addEventListener('click', function () {
  document.querySelectorAll('.language-selector').forEach(s => s.classList.remove('open'));
});


